package dto;

public class Event_DTO {
	private int e_no = 0;
	private String e_name = null;
	private String e_coupon = null;
	private String e_id = null;
	
	public int getE_no() {
		return e_no;
	}
	public void setE_no(int e_no) {
		this.e_no = e_no;
	}
	public String getE_name() {
		return e_name;
	}
	public void setE_name(String e_name) {
		this.e_name = e_name;
	}
	public String getE_coupon() {
		return e_coupon;
	}
	public void setE_coupon(String e_coupon) {
		this.e_coupon = e_coupon;
	}
	public String getE_id() {
		return e_id;
	}
	public void setE_id(String e_id) {
		this.e_id = e_id;
	}
	
}
