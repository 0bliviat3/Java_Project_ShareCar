package dto;

public class Car_DTO {
	private int c_no = 0;
	private String c_m_id = null;
	private String c_num = null;
	private int c_years = 0;
	private String c_kind = null;
	private String c_value = null;
	private int c_term = 0;
	private String c_date = null;
	
	public int getC_no() {
		return c_no;
	}
	public void setC_no(int c_no) {
		this.c_no = c_no;
	}
	public String getC_m_id() {
		return c_m_id;
	}
	public void setC_m_id(String c_m_id) {
		this.c_m_id = c_m_id;
	}
	public String getC_num() {
		return c_num;
	}
	public void setC_num(String c_num) {
		this.c_num = c_num;
	}
	public int getC_years() {
		return c_years;
	}
	public void setC_years(int c_years) {
		this.c_years = c_years;
	}
	public String getC_kind() {
		return c_kind;
	}
	public void setC_kind(String c_kind) {
		this.c_kind = c_kind;
	}
	public String getC_value() {
		return c_value;
	}
	public void setC_value(String c_value) {
		this.c_value = c_value;
	}
	public int getC_term() {
		return c_term;
	}
	public void setC_term(int c_term) {
		this.c_term = c_term;
	}
	public String getC_date() {
		return c_date;
	}
	public void setC_date(String c_date) {
		this.c_date = c_date;
	}
	
}
